CREATE TABLE RETIRE_USERS (
	USER_NO 	NUMBER	           NOT NULL,
	EMAIL	    VARCHAR2 (100 BYTE) NOT NULL,
	RETIRE_DATE	DATE               NOT NULL,
	ARTIST	    VARCHAR2 (45 BYTE) NOT NULL

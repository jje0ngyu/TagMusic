	BIRTHYEAR	      VARCHAR2 (4 BYTE),
	BIRTHDAY	      VARCHAR2 (4 BYTE),
	POSTCODE	      VARCHAR2 (5 BYTE),
	ROAD_ADDRESS	  VARCHAR2 (100 BYTE),
	JIBUN_ADDRESS	  VARCHAR2 (100 BYTE),
	DETAIL_ADDRESS    VARCHAR2 (100 BYTE),
	EXTRA_ADDRESS	  VARCHAR2 (100 BYTE),
	AGREE_CODE	      NUMBER,
